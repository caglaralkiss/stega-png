import { populateWithOpacityValues } from '../util'

describe('Util', () => {
	test('should populate stego buffer with opacity values from original buffer', () => {
		const originalBuffer = Buffer.from([0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C])
		const stegoBuffer = Buffer.from([0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF, 0xA1, 0xA2, 0xA3])

		const stegoBufferWithOpacity = Buffer.from([0xAA, 0xBB, 0xCC, 0x04, 0xDD, 0xEE, 0xFF, 0x08, 0xA1, 0xA2, 0xA3, 0x0C])
		expect(populateWithOpacityValues(originalBuffer, stegoBuffer)).toEqual(stegoBufferWithOpacity)
	})
})
